// Search methods
const getCountryByCode = (code) => {
    if (!code) return null;
    return countries.find((country) => country.code.toLowerCase() === code.toLowerCase()) || null;
};

const getCountryByName = (name) => {
    if (!name) return null;
    return countries.find((country) => country.name.toLowerCase() === name.toLowerCase()) || null;
};

const getCountryByDialCode = (dialCode) => {
    if (!dialCode) return null;
    const cleanDialCode = dialCode.startsWith('+') ? dialCode : `+${dialCode}`;
    return countries.find((country) => country.dialCode === cleanDialCode) || null;
};

const searchCountriesByName = (searchTerm) => {
    if (!searchTerm) return [];
    const term = searchTerm.toLowerCase();
    return countries.filter((country) => country.name.toLowerCase().includes(term));
};

module.exports = {
    countries,
    getCountryByCode,
    getCountryByName,
    getCountryByDialCode,
    searchCountriesByName,
    // For backward compatibility
    default: countries,
};

// Also export as default for compatibility
module.exports.default = countries;
